    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        
        if(Ano_carro.getText().isEmpty() || cor.getText().isEmpty() || fabricante.getText().isEmpty()
            || modelo.getText().isEmpty() || chassi.getText().isEmpty() || preco.getText().isEmpty() || imagem.getIcon() == null){
            
          JOptionPane.showMessageDialog(null, "Digite todos os campos!", "Vilet's ConcessionÃ¡ria", JOptionPane.INFORMATION_MESSAGE);
            
        } else{
        
        VeiculoDAO dao = new VeiculoDAO();
        Veiculo vei = new Veiculo();
        
        vei.setAno(Ano_carro.getText());
        vei.setCor(cor.getText());
        vei.setFabricante(fabricante.getText());
        vei.setModelo(modelo.getText());
        vei.setNumChassi(chassi.getText());
        vei.setPreco(Integer.parseInt(preco.getText()));
        vei.setDescricao(descricao.getText());
        
        dao.cadastrarVeiculo(vei);
        }
        
    }//GEN-LAST:event_enviarActionPerformed

